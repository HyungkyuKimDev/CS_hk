# - バックグラウンドで実行 "&"
# &とはコマンドの最後のつけるとそのコマンドをバックグラウンドで実行するコマンドだ。

# &の使い方
# コマンドの最後に＆をつける
sleep 3000 &
# sleepという時間を止まる命令をバックグラウンドで実行する。

# 作業を管理する方法

# jobs　// 現在利用者の作業を表示
jobs
# その結果：作業が表示

# fg <jobsの番号＞ // バックグラウンドの作業をフォアグラウンドに変更
fg 1 
# その結果：jobsの１番の作業をフォアグラウンドに変更

# bg <jobsの番号）//停止されているフォアグラウンドの作業をバックグラウンドに変更
bg 1
# その結果：jobsの１番の作業をバックグラウンドに変更