2.Linuxの定義されている環境変数

環境変数名 / 保存されていたデータ
HOME / ログインユーザのホームディレクトリ
PWD / 現在のディレクトリ
USER / ログインユーザの名
HOSTNAME ／コンピュータのホスト名
PATH / コマンド・プログラムを検索するディレクトリ
HISTSIZE / コマンド履歴の保存件数
LANG / 使用する言語
SHELL / ログインユーザのシェル
END

# 例） ログインユーザのシェルを出力
echo $SHELL
# その結果：/bin/bash